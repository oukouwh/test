# random随机数
import random
# random.random()生成一个小于1.0大于0.0的随机浮点数
print(random.random())  # 0.23135921145416383
# random.uniform(a,b) 
print(random.uniform(1,10)) # 8.122329393953343
print(random.triangular()) # 0.2737888826824448
print(random.triangular(1,3)) # 1.8371482025921404
print(random.triangular(1,5,10)) # 5.5982201024543965