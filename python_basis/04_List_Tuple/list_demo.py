# 2020/05/01,资本论,45.60,10
# 2020/05/02,百年孤独,39.90,12
# 2020/05/02,追风筝的人,30.50,8
# 2020/05/03,人生海海,40.00,5
# 2020/05/03,人间失格,20.80,5
# 总数量:45,总金额:1832.80
