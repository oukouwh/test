from tkinter import *
from tkinter import ttk
# 树状结构案例2
root = Tk()
root.title('树状结构案例2')
tree = ttk.Treeview(root)
tree['columns'] = ('one','two','three','four','five')
tree.column('one',width=100)
tree.column('two',width=100)
tree.column('three',width=100)
tree.column('four',width=100)
tree.column('five',width=100)
tree.heading('one',text='姓名')
tree.heading('two',text='性别')
tree.heading('three',text='年龄')
tree.heading('four',text='地址')
tree.heading('five',text='手机号')
tree.insert('',0,text='部长',values=('王部长','男','32','中国上海','13888889999'))
tree.insert('',1,text='科长',values=('科长','女','45','中国北京','13834567890'))
tree.insert('',2,text='HR',values=('刘明','男','30','中国湖北','13123567908'))
tree.insert('',3,text='COO',values=('COO','男','32','中国上海','13888889999'))
tree.insert('',4,text='CEO',values=('CEO','男','30','中国上海','13888889999'))
tree.insert('',5,text='CMO',values=('CMO','男','39','中国上海','13888889999'))
tree.pack()
root.mainloop()
